const quoteLineItem = (
  id: string,
  quoteId: string,
  type: "service" | "addon",
  templateId: string | null,
  addonId: string | null,
  tier: "good" | "better" | "best" | null,
  label: string,
  basePrice: number,
  mult: number,
  createdAt: string
): QuoteLineItem => ({
  id,
  quoteId,
  type,
  quoteTemplateId: templateId,
  quoteAddonId: addonId,
  tier,
  label,
  basePrice,
  multiplierApplied: mult,
  finalPrice: Math.round(basePrice * mult * 100) / 100,
  createdAt,
});

